// This syntax got nothin to do with the guessTheNumber-Game
// It simply shows how to join strings together 
// If you type into the console: name + hello, it shows the same output as greeting
const name = "Bingo";
name;
const hello = " says hello!";
hello;
const greeting = name + hello;
greeting;

// There are also some shortcut operators
// For example to add a new value to an already existing value
let name1 = "Bingo";
name1 += " says Hello";

// The example above has the same output as the following, only that the syntax is much shorter
let name2 = "Bingo";
name2 = name2 + " says hello";